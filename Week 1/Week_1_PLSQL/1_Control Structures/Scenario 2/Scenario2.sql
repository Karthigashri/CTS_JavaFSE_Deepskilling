BEGIN
  FOR cust IN (SELECT CustomerID FROM Customers WHERE BALANCE > 10000) LOOP
    UPDATE Customers
    SET IsVIP = 'TRUE'
    WHERE CustomerID = cust.CustomerID;
  END LOOP;
  DBMS_OUTPUT.PUT_LINE('VIP status updated for eligible customers.');
END;
