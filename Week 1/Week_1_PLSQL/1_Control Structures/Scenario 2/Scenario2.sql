BEGIN
  EXECUTE IMMEDIATE 'DROP TABLE Customers CASCADE CONSTRAINTS';
EXCEPTION WHEN OTHERS THEN IF SQLCODE != -942 THEN RAISE; END IF;
END;
/

CREATE TABLE Customers (
  CustomerID NUMBER PRIMARY KEY,
  NAME VARCHAR2(100),
  AGE NUMBER,
  BALANCE NUMBER,
  IsVIP VARCHAR2(5)
);
/

